package de.seipler.util.swing.layout;

/**
 * 
 * @author Georg Seipler
 */
public class LayoutWrapperException extends RuntimeException {

  public LayoutWrapperException() {
    super();
  }

  public LayoutWrapperException(String message) {
    super(message);
  }

  public LayoutWrapperException(Throwable cause) {
    super(cause);
  }

  public LayoutWrapperException(String message, Throwable cause) {
    super(message, cause);
  }

}
