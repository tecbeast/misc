package de.seipler.util.swing.layout;

/**
 * 
 * @author Georg Seipler
 */
public class XmlLayoutException extends RuntimeException {

  public XmlLayoutException() {
    super();
  }

  public XmlLayoutException(String message) {
    super(message);
  }

  public XmlLayoutException(Throwable cause) {
    super(cause);
  }

  public XmlLayoutException(String message, Throwable cause) {
    super(message, cause);
  }

}
